package com.integrosys.simulator.host.transformer;

import java.util.ArrayList;
import java.util.List;

public class OBFixedLengthDefinition {

	private String code;

	private String strClassName;

	private int totalColumnLength;

	private ArrayList column;

	public OBFixedLengthDefinition() {

	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getStrClassName() {
		return strClassName;
	}

	public void setStrClassName(String strClassName) {
		this.strClassName = strClassName;
	}

	public int getTotalColumnLength() {
		return totalColumnLength;
	}

	public void setTotalColumnLength(int totalColumnLength) {
		this.totalColumnLength = totalColumnLength;
	}

	public List getColumn() {
		return column;
	}

	public void setColumn(ArrayList column) {
		this.column = column;
	}

}
