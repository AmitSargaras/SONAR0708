	TYPE_DEED_ASSN_CODE VARCHAR(10),
	DOCUMENT_DATE TIMESTAMP,
	DOCUMENT_AMT_CCY CHAR(3),
	DOCUMENT_AMT DECIMAL(20,2),
	ISDA_AGREEMENT_DATE TIMESTAMP,
	IFEMA_AGREEMENT_DATE TIMESTAMP,
	ICOM_DOCUMENT_DATE TIMESTAMP,
	ISDA_DESC VARCHAR(20),
	REF_NUMBER VARCHAR(100),
