	MIN_AMT_CCY CHAR(3),
	MIN_AMT DECIMAL(30,2),
	MAX_AMT_CCY CHAR(3),
	MAX_AMT DECIMAL(30,2),
	CMS_COLLATERAL_ID BIGINT,
	CMS_COLLATERAL_STG_ID BIGINT,
	SOURCE_ID VARCHAR(10),
	REGION VARCHAR(50) NOT NULL DEFAULT 'NA',
	VALID_IND CHAR(1) DEFAULT 'Y'
