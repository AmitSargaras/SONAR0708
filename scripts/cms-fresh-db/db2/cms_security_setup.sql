begin atomic

 DECLARE groupid BIGINT DEFAULT 20070103000001;--
 Declare id BIGINT default 20070103000001;--
 declare trxid BIGINT default 20070103000001;--
 
   FOR i AS SELECT distinct s.security_type_id as security_type_id FROM cms_security_sub_type s 
	  do
     FOR j AS SELECT country_iso_code FROM country
		 do
			for k as select st.security_sub_type_id from cms_security_sub_type st where st.SECURITY_TYPE_ID = i.security_type_id
			do
      	INSERT INTO cms_security_parameter (id, country_iso_code, security_sub_type_id, threshold_percent, valuation_frequency_unit, valuation_frequency, group_id, version_time, status)
       	VALUES (id, j.COUNTRY_ISO_CODE, k.security_sub_type_id, null, null, null, groupid, null, 'ACTIVE');--
				
   			set id = id + 1;--
			end for;--
			
			INSERT INTO TRANSACTION 
			( TRANSACTION_ID, FROM_STATE, USER_ID, TRANSACTION_TYPE, CREATION_DATE,
			TRANSACTION_DATE, REFERENCE_ID, STATUS, STAGING_REFERENCE_ID, TEAM_ID, 
			VERSION, REMARKS,TRX_REFERENCE_ID, OPSDESC, LEGAL_NAME, LEGAL_ID, 
			CUSTOMER_NAME, CUSTOMER_ID, TRX_ORIGIN_COUNTRY,TRX_ORIGIN_ORGANISATION, 
			LIMIT_PROFILE_ID, TRX_SEGMENT )
			VALUES 
			( char(trxid) , 'ND', 0, 'COLPARAM',  current timestamp,  current timestamp, 
			groupid, 'ACTIVE', groupid, 0, NULL, NULL, NULL, 'SYSTEM_CREATION', 
			NULL, NULL, NULL, NULL, NULL, NULL, NULL, NULL);--
	    			
			set groupid = groupid + 1;--					
			set trxid = trxid + 1;--
   END FOR;--
  END FOR;--
	
	insert into cms_stage_security_parameter (id, country_iso_code, security_sub_type_id, threshold_percent, valuation_frequency_unit, valuation_frequency, group_id, version_time, status)
	select id, country_iso_code, security_sub_type_id, threshold_percent, valuation_frequency_unit, valuation_frequency, group_id, version_time, status
	from cms_security_parameter;--
END;

